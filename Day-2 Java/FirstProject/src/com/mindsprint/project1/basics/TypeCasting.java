package com.mindsprint.project1.basics;

public class TypeCasting {

    public static void main(String[] args) {

        short data =10;
        float data1=data;

        System.out.println("Data "+data+" "+data1);

        double info=78.78;
        int info1=(int) info;
        System.out.println("Info "+info+" "+info1);

        char a='A';
        int b=a;
        System.out.println("The data a "+a+" "+b);

        char d= 'a';
        System.out.println((int)d);


    }
}
